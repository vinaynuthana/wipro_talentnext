package emp_details;

public class Person {
	    private String name;

	    // Constructor
	    public Person(String name) {
	        this.name = name;
	    }

	    // Getter
	    public String getName() {
	        return name;
	    }

	    // Setter
	    public void setName(String name) {
	        this.name = name;
	    }
	}


